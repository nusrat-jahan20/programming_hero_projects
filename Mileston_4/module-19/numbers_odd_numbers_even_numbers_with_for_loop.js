// natural number 1 to 10
for(var number = 1; number <= 10; number++) {
    console.log(number);
}

// natural even number 1 to 10
for(var number = 0; number <= 10; number+=2) {
    console.log(number);
}

for(var number = 0; number <= 10; number++) {
    if(number % 2 == 0) {
        console.log(number);
    }
}

// natural odd number 1 to 10
for(var number = 1; number <= 10; number+=2) {
    console.log(number);
}

for(var number = 0; number <= 10; number++) {
    if(number % 2 == 1) {
        console.log(number);
    }
}


